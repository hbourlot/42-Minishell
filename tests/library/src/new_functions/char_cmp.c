#include "libft.h"

/// @brief Compares two characters for equality.
/// @param a The first character to compare.
/// @param b The second character to compare.
/// @return 1 if the characters are equal, 0 otherwise.
int	char_cmp(char a, char b)
{
	return (a == b);
}
